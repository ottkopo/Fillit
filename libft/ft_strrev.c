#include "libft.h"

char		*ft_strrev(char *s)
{
	int i;
	int j;

	i = ft_strlen(s) - 1;
	j = 0;
	while (i > j)
	{
		ft_swap(&s[i], &s[j]);
		i--;
		j++;
	}
	return (s);
}
